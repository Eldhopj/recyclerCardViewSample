package com.example.eldhopjames.recyclercardviewsample;

public class ModelClass {
    private String head;
    private String desc;

    public ModelClass() {
    }

    public ModelClass(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {
        return desc;
    }
}
